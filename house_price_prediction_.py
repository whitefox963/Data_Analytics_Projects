import streamlit as st
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_absolute_error

st.title('House Price Prediction')

uploaded_file = st.file_uploader("Upload a CSV file", type=['csv'])

url = 'https://raw.githubusercontent.com/YBIFoundation/Live-Projects/main/IndiaHousePrice.csv'

# Read the CSV file
df = pd.read_csv(url)

df.columns

y = df['Price']
x = df[['Number of bedrooms', 'Number of bathrooms', 'Living area sqft',
       'Number of schools nearby', 'Distance from the airport']]

x_train,x_test,y_train,y_test=train_test_split(x,y,train_size=0.8,random_state=2529)

model = LinearRegression()
model.fit(x_train,y_train)

y_pred = model.predict(x_test)
mean_absolute_error(y_test,y_pred)

st.write("Make Predictions")
bedrooms =st.number_input("Number of bedrooms", min_value=1, max_value=10, step=1)
bathrooms = st.number_input("Number of bathrooms", min_value=1, max_value=10, step=1)
living_area = st.number_input("Living area in square feet", min_value=100, max_value=10000, step=10)
schools_nearby = st.number_input("Number of schools nearby",min_value=0,step=1)
distance_airport = st.number_input("Distance from the airport in KM",min_value=0.0,step=0.1)

input_data = pd.DataFrame({
    'Number of bedrooms': [bedrooms],
    'Number of bathrooms': [bathrooms],
    'Living area sqft': [living_area],
    'Number of schools nearby': [schools_nearby],
    'Distance from the airport': [distance_airport]
})
model.predict(input_data)